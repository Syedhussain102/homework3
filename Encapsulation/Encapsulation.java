package encapsulaiton;

public class Encapsulation {
	
	private int a;
	private int b ;
	private String x;
	
	
	
	public int getC() {
		return a;
	}
	public void setC(int a) {
		this.a = a;
	}
	public int getD() {
		return b;
	}
	public void setD(int b) {
		this.b = b;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	
	
	

}
